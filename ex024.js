/*Funções parametrizadas - Menor Número
Escreva uma função chamada menorNumero. Ela deve receber dois números como parâmetro e retornar
o menor entre eles. Caso os números sejam iguais, basta que se retorne qualquer um deles.*/

function menorNumero(a,b){
    var a = 5
    var b = 10
    if(a < b){
      return a
    }else{
      return b
    }
}

